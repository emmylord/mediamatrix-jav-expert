# ======================================================================
# 参数化测试
# ======================================================================

@pytest.mark.parametrize("filename, expected", _HITS, ids=[h[0] for h in _HITS])
def test_hit(filename: str, expected: str):
    """文件名应被识别为 AV 电影，并提取出正确番号。"""
    result = _extract(filename)
    assert result == expected, (
        f"\n文件名: {filename!r}\n期望番号: {expected!r}\n实际结果: {result!r}"
    )


@pytest.mark.parametrize("filename", _MISSES, ids=_MISSES)
def test_miss(filename: str):
    """文件名不应被识别为 AV 电影（避免误伤普通内容）。"""
    result = _extract(filename)
    assert result is None, (
        f"\n文件名: {filename!r}\n期望 None，实际误识别为: {result!r}"
    )
